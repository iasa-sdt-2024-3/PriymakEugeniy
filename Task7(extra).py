#Функція, що конвертує 32-бітове число в рядкове представлення IPv4 адреси
def int32_to_ip(n):
    return '.'.join([str((n >> (i * 8)) & 0xFF) for i in range(4)[::-1]])

# Приклади використання
print(int32_to_ip(2149583361))  # "128.32.10.1"
print(int32_to_ip(32))  # "0.0.0.32"
print(int32_to_ip(0))  # "0.0.0.0"